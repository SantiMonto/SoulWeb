import mongoose from 'mongoose';
import { ProjectModel } from '../proyecto/project.js'
import { UserModel } from '../usuario/user.js'

const { Schema, model } = mongoose;

const avanceSchema = new Schema({
  fecha: {
    type: Date,
    required: true,
  },
  descripcion: {
    type: String,
    required: true,
  },
  observaciones: [
    {
      type: String,
    },
  ],
  proyecto: {
    type: Schema.Types.ObjectId,
    ref: ProjectModel,
    required: true,
  },
  creadoPor: {
    type: Schema.Types.ObjectId,
    ref: UserModel,
    required: true,
  },
});

const AvanceModel = model('Avance', avanceSchema);

export { AvanceModel };
